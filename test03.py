# คำสั่งแสดงผลข้อมูลใดๆ ทางหน้าจอ ใช้ฟังก์ชั่น print()
# string
print('Hello')
print('Hi')
print(''''Yo''')
print('''HURA''')

# number
print(123) #integer/int
print(123,456) #float/real

# boolean ตรรกะ มีแค่ true กับ false
print(True)
print(False)

#Expression นิพจน์ คือ อะไรกฌตามที่มี process การทำงาน เช่น การคำนวณ ตัวแปร หรืออื่นๆ
print(1+25) #การบาก
print(10-2) #การลบ 
print(2*3) #การคูณ
print(10/2) #การหาร
print(10%3) #การหารเอาเศษ
